
package one;

public class UnaryDemo {
    public static void main(String[] args) {
        int result = +2;
        result = result + 2;
        System.out.println(result ++);
        System.out.println(result);
        System.out.println( ++ result);
        int result1 = 4;
        result1 = result1 - 1;
        System.out.println(result1 --);
        System.out.println(result1);
        System.out.println(-- result1);
        boolean uspio = false;
        System.out.println(! uspio);
        boolean nijeUspio = false;
        System.out.println(! nijeUspio);
        
        
        
    }
    
    
}
