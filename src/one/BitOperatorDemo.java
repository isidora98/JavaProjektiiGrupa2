
package one;
public class BitOperatorDemo {
    public static void main(String[] args) {
        int a=5;
        int b=7;
        int result=a|b;
        System.out.println(result);
        int result1=a&b;
        System.out.println(result1);
        int result2=a^b;
        System.out.println(result2);
        int result3=~a;
        System.out.println(result3);
        
        
    }
    
}
